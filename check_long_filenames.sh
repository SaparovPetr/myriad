#!/bin/bash

# Проверяем, был ли передан аргумент (путь к директории)
if [ -z "$1" ]; then
  echo "Пожалуйста, укажите путь к директории."
  exit 1
fi

# Путь к директории
DIR="$1"

# Проверяем, существует ли указанная директория
if [ ! -d "$DIR" ]; then
  echo "Указанная директория не существует."
  exit 1
fi

# Создаем новую директорию для файлов с длинными именами
LONG_DIR="$DIR/long_filenames"
if [ ! -d "$LONG_DIR" ]; then
  mkdir "$LONG_DIR"
  echo "Создана директория для файлов с длинными именами: $LONG_DIR"
fi

# Логируем файлы с именами длиннее 150 символов и перемещаем их в новую директорию
echo "Проверка файлов с именами длиннее 150 символов в директории $DIR..." > check_files.log
find "$DIR" -type f | while read -r file; do
  # Получаем длину имени файла
  filename=$(basename "$file")
  filename_length=${#filename}
  
  # Если имя файла длиннее 150 символов, логируем и перемещаем его
  if [ "$filename_length" -gt 150 ]; then
    # Логируем файл
    echo "Файл с длинным именем: $file (длина имени: $filename_length символов)" >> check_files.log
    
    # Перемещаем файл в новую директорию
    mv "$file" "$LONG_DIR/"
    echo "Перемещен в $LONG_DIR: $file"
  fi
done

# Выводим сообщение о завершении
echo "Проверка завершена. Результаты в check_files.log."
